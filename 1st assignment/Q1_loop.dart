void main() {
  print("==============================\nFor Loop\n");
  for (int i = 0; i < 5; i++) {
    print("Hello Sunway");
  }
  print("\n============================\nWHile Loop\n");
  int i=0;
  while (i < 5) {
    print("Hello Sunway");
    i++;
  }
  print("============================\nDo While Loop\n");
  int loop = 0;
  do {
    print("Hello Sunway");
    loop++;
  } while (loop < 3);
}
